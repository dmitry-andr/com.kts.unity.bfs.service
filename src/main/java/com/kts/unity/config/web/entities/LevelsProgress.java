package com.kts.unity.config.web.entities;

public class LevelsProgress {

	private int playerId;
	private String levelsProgressData;
	private int levelsProgressRankingPoints;
	
	
	public int getPlayerId() {
		return playerId;
	}
	public void setPlayerId(int playerId) {
		this.playerId = playerId;
	}
	public String getLevelsProgressData() {
		return levelsProgressData;
	}
	public void setLevelsProgressData(String levelsProgressData) {
		this.levelsProgressData = levelsProgressData;
	}
	public int getLevelsProgressRankingPoints() {
		return levelsProgressRankingPoints;
	}
	public void setLevelsProgressRankingPoints(int levelsProgressRankingPoints) {
		this.levelsProgressRankingPoints = levelsProgressRankingPoints;
	}
	
	

}
